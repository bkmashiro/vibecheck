type RoastLevel = 'god' | 'heavy' | 'mixed' | 'human'

function getRoastLevel(score: number): RoastLevel {
  if (score >= 2000) return 'god'
  if (score >= 500)  return 'heavy'
  if (score >= 100)  return 'mixed'
  return 'human'
}

